export type Lang = 'ru' | 'kk';

/** Определить язык по буквам, специфичным для казахского. */
const KZ_LETTERS = /[әғқңөұүһі]/i;
export function detectLang(text: string): Lang {
  return KZ_LETTERS.test(text) ? 'kk' : 'ru';
}

